int get_count(int x, int y, int k){
    int ret = 0;
    for(int i=0;i<4;i++){
        for(int j=0;j<4;j++){
            //if(map[i+x][j+y] > 0)
            ret += (block[k][i][j] * map[i+x][j+y]);
            //cout<<ret<<" : "<<block[k][i][j] << " * "<< map[i+x][j+y]<<"\n";
        }
    }
    return ret;
}

